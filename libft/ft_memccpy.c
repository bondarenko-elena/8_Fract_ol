#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t size)
{
	size_t			i;
	unsigned char	*s;
	unsigned char	*d;

	i = 0;
	s = (unsigned char *)src;
	d = (unsigned char *)dst;
	while (i < size)
	{
		d[i] = s[i];
		if (s[i] == (unsigned char)c)
			return ((void*)&dst[i + 1]);
		i++;
	}
	return (NULL);
}
